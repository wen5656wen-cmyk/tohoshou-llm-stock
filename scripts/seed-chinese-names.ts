// ── 主函数 ───────────────────────────────────────────────────────────────────
async function main() {
  console.log("=== 填充股票中文名 ===\n");

  const symbols = Object.keys(CHINESE_NAMES);
  let updated = 0, notFound = 0;

  for (const symbol of symbols) {
    const nameZh = CHINESE_NAMES[symbol];
    const result = await prisma.stock.updateMany({
      where: { symbol },
      data: { nameZh },
    });

    if (result.count > 0) {
      // Also update StockScore if it exists
      await prisma.stockScore.updateMany({
        where: { symbol },
        data: { nameZh },
      });
      updated++;
      console.log(`  ✓ ${symbol}  →  ${nameZh}`);
    } else {
      notFound++;
      console.log(`  ○ ${symbol}  （不在 DB 中，跳过）`);
    }
  }

  console.log(`\n完成：更新 ${updated} 只，未找到 ${notFound} 只`);

  // 验证
  const withZh = await prisma.stock.count({ where: { nameZh: { not: null } } });
  console.log(`Stock 表中 nameZh 非空：${withZh} 只`);

  await prisma.$disconnect();
}

main().catch((e) => {
  console.error(e);
  prisma.$disconnect();
  process.exit(1);
});
