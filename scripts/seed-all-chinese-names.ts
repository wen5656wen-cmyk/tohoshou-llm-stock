// 4. 主函数
// ═══════════════════════════════════════════════════════════════════════════════
async function main() {
  console.log("=== 全量股票中文名填充 v5.8.0 ===");
  console.log(`模式: ${FORCE ? "强制覆盖（--force）" : "跳过已有中文名"}\n`);

  const allStocks = await prisma.stock.findMany({
    select: { symbol: true, name: true, nameZh: true },
  });

  let manualCount = 0;
  let autoCount = 0;
  let skipCount = 0;
  const failedSymbols: string[] = [];

  for (const stock of allStocks) {
    const { symbol, name, nameZh: existingZh } = stock;

    let newZh: string | null = null;
    let source: "manual" | "auto" | "skip" = "skip";

    if (MANUAL_MAP[symbol]) {
      // 手动映射始终优先（即使已有中文名也覆盖）
      newZh = MANUAL_MAP[symbol];
      source = "manual";
    } else if (existingZh && !FORCE) {
      // 已有中文名，非 --force 模式跳过
      source = "skip";
    } else {
      // 自动翻译
      const translated = translateName(name);
      if (translated && translated !== name) {
        newZh = translated;
        source = "auto";
      } else if (translated) {
        // 即使翻译结果与原名相同（如全角英文名），也设置（确保100%覆盖）
        newZh = translated;
        source = "auto";
      }
    }

    if (source === "skip") {
      skipCount++;
      continue;
    }

    if (!newZh) {
      // 兜底：使用全角归一化后的原名
      newZh = normalizeFullWidth(name);
      source = "auto";
    }

    try {
      await prisma.stock.update({
        where: { symbol },
        data: { nameZh: newZh },
      });
      // 同步更新 StockScore
      await prisma.stockScore.updateMany({
        where: { symbol },
        data: { nameZh: newZh },
      });
      if (source === "manual") manualCount++;
      else autoCount++;
    } catch (e) {
      console.error(`  ✗ ${symbol} 更新失败:`, (e as Error).message);
      failedSymbols.push(symbol);
    }
  }

  // ── 统计报告 ───────────────────────────────────────────────────────────────
  const total = allStocks.length;
  const withZh = await prisma.stock.count({ where: { nameZh: { not: null } } });

  console.log("\n═══════════════════════════════");
  console.log(`总股票数:        ${total}`);
  console.log(`手动指定更新:    ${manualCount}`);
  console.log(`自动翻译新增:    ${autoCount}`);
  console.log(`跳过（已有）:    ${skipCount}`);
  console.log(`失败:            ${failedSymbols.length}`);
  console.log("───────────────────────────────");
  console.log(`Stock.nameZh 覆盖: ${withZh} / ${total} (${((withZh / total) * 100).toFixed(1)}%)`);
  console.log("═══════════════════════════════");

  if (failedSymbols.length > 0) {
    console.log("\n失败列表:", failedSymbols.join(", "));
  }

  await prisma.$disconnect();
}

main().catch((e) => {
  console.error(e);
  prisma.$disconnect();
  process.exit(1);
});
