/** 全角ASCII → 半角 */
function normalizeFullWidth(s: string): string {
  return s
    .replace(/[Ａ-Ｚ]/g, (c) => String.fromCharCode(c.charCodeAt(0) - 0xfee0))
    .replace(/[ａ-ｚ]/g, (c) => String.fromCharCode(c.charCodeAt(0) - 0xfee0))
    .replace(/[０-９]/g, (c) => String.fromCharCode(c.charCodeAt(0) - 0xfee0))
    .replace(/　/g, " ")
    .trim();
}

/** 去除公司形态后缀/前缀 */
function removeCorporateForm(s: string): string {
  const forms = [
    "株式会社", "（株）", "（株)", "(株）", "(株)", "㈱",
    "有限会社", "合同会社", "合資会社", "合名会社", "相互会社",
  ];
  let r = s;
  for (const f of forms) {
    r = r.replaceAll(f, "");
  }
  return r.trim();
}

/** 主翻译函数 */
function translateName(jaName: string): string {
  let name = jaName;

  // Step 1: 全角归一化
  name = normalizeFullWidth(name);

  // Step 2: 去除公司形态
  name = removeCorporateForm(name);

  // Step 3: 日语词组 → 中文（复合词先处理）
  for (const [ja, zh] of WORD_MAP) {
    name = name.replaceAll(ja, zh);
  }

  // Step 4: 片假名 → 中文（按长度降序已排列）
  for (const [kata, zh] of KATAKANA_MAP) {
    name = name.replaceAll(kata, zh);
  }

  // Step 5: 汉字字符替换（简体化）
  for (const [ja, zh] of KANJI_MAP) {
    name = name.replaceAll(ja, zh);
  }

  // Step 6: 清理多余空格
  name = name.replace(/\s+/g, " ").trim();

  // Fallback: 翻译结果为空则返回原名（全角归一化后）
  return name || normalizeFullWidth(jaName);
}

